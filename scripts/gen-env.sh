CMC_API_KEY=$(pass layer3/pro.coinmarketcap.com/mod@yellow.org/apikey)

# ── Notion task sync ──────────────────────────────────────────────────────────
NOTION_API_KEY=$(pass layer3/notion.so/mod@yellow.org/apikey)
NOTION_DATABASE_ID=31f75b8f-eb04-80bd-ae9d-000b93bf0dbd

# ── EVM queries (cast) ────────────────────────────────────────────────────────
ETH_RPC_URL=$(pass layer3/yellow/mainnet/infura/rpc_wss_url)

# ── Runtime paths (set by start_gateway.sh — do not override here) ───────────
# MESSAGING_CWD is set automatically by scripts/start_gateway.sh
EOF
  echo "Written to ~/.hermes/.env" >&2
  exit 0
fi

cat <<EOF
SSH_PUBLIC_KEY="$(ssh-add -L | head -1)"
